import java.util.*;
class  for_loop
{
	static Scanner sc=new Scanner(System.in);
	public static void main(String[] args) 
	{
		System.out.println("enter the value");
		int n=sc.nextInt();
		for(int i=1;i<=n;i++)
		System.out.println(i);
	}
}


//print the all natural number for 1 to n (use for loop)