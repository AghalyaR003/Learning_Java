class typecasting_operator   // operator first type is typecasting
{
	public static void main(String[] args) 
	{
		int a=10;         // int to float
		float res=a;
		System.out.println("the typecasting value is:"+res);
		byte b= 20;           //byte to short
		short res1=b;
		System.out.println("the typecasting value is:"+res1);
		short c=30;            //short to long
		long res2=c;
		System.out.println("the typecasting value is:"+res2);
		long d=39877;      //long to float
		float res4=d;
		System.out.println("the typecasting value is:"+res4);

	}
}
